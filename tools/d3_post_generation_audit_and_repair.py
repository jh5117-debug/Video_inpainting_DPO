#!/usr/bin/env python3
"""Audit and repair D3 YouTube-VOS partial-mask generated-loser manifests.

D3 is the target-domain generated-loser dataset.  The implementation reuses
the D2 post-generation checks, but changes the dataset metadata and defaults
to the YouTube-VOS D3 root.  It never overwrites original manifests; repaired
copies are written as `*.repaired.jsonl`.
"""

from __future__ import annotations

import argparse
import json
from collections import Counter
from pathlib import Path

import tools.d2_post_generation_audit_and_repair as d2


D3_ROOT = (
    "/mnt/workspace/hj/nas_hj/H20_Video_inpainting_DPO/data/generated_losers/"
    "official_videodpo_diffueraser_youtubevos_partialmask_loser_k4"
)


def repair_row(row, manifest_name, stack, prior, args):  # type: ignore[no-untyped-def]
    repaired = d2.repair_row(row, manifest_name, stack, prior, args)
    repaired["source_dataset"] = "youtubevos"
    repaired["target_domain"] = "youtubevos"
    repaired["data_asset"] = args.data_asset
    return repaired


def build_parser() -> argparse.ArgumentParser:
    parser = argparse.ArgumentParser(description=__doc__)
    parser.add_argument("--output_root", default=D3_ROOT)
    parser.add_argument("--sample_size", type=int, default=100)
    parser.add_argument("--seed", type=int, default=20260602)
    parser.add_argument("--canonical_num_frames", type=int, default=16)
    parser.add_argument("--canonical_height", type=int, default=320)
    parser.add_argument("--canonical_width", type=int, default=512)
    parser.add_argument("--data_asset", default="D3_youtubevos_partialmask_k4_diffueraser_only")
    parser.add_argument("--diffueraser_inference_stack", default="auto", choices=["auto", "or", "br", "unconfirmed"])
    parser.add_argument("--diffueraser_prior_mode", default="auto", choices=["auto", "propainter", "noise", "none", "unconfirmed"])
    parser.add_argument("--report", default="")
    parser.add_argument("--no_repair", action="store_true")
    return parser


def write_report(
    path: Path,
    output_root: Path,
    manifest_counts,
    errors,
    counters,
    path_checks,
    consistency,
    sample,
    stack,
    prior,
    evidence,
    repaired_files,
) -> None:  # type: ignore[no-untyped-def]
    lines = [
        "# D3 Post-Generation Audit",
        "",
        f"- output_root: `{output_root}`",
        f"- repaired_at: generated by `tools/d3_post_generation_audit_and_repair.py`",
        "- source_dataset: `youtubevos`",
        "- purpose: target-domain DPO data readiness; do not launch training from this report alone.",
        f"- diffueraser_inference_stack: `{stack}`",
        f"- diffueraser_prior_mode: `{prior}`",
        "",
        "## Manifest Counts",
        "",
    ]
    for name, count in manifest_counts.items():
        lines.append(f"- `{name}`: {count}")
    lines.extend(["", "## Metadata Counters", ""])
    for name, counter in counters.items():
        lines.append(f"- {name}: `{counter}`")
    lines.extend(["", "## Stack/Prior Evidence", ""])
    for item in evidence:
        lines.append(f"- {item}")
    lines.extend(["", "## Path Audit", "", "```json"])
    lines.append(json.dumps(path_checks, ensure_ascii=False, indent=2, sort_keys=True))
    lines.extend(["```", "", "## Selected Manifest Consistency", "", "```json"])
    lines.append(json.dumps(consistency, ensure_ascii=False, indent=2, sort_keys=True))
    lines.extend(["```", "", "## Random Sample Decode And Comp Audit", "", "```json"])
    lines.append(json.dumps(sample, ensure_ascii=False, indent=2, sort_keys=True))
    lines.extend(["```", "", "## JSONL Parse Errors", ""])
    if any(errors.values()):
        for name, items in errors.items():
            for item in items[:50]:
                lines.append(f"- {name}: {item}")
    else:
        lines.append("- none")
    lines.extend(["", "## Repaired Manifests", ""])
    for repaired in repaired_files:
        lines.append(f"- `{repaired}`")
    lines.extend(
        [
            "",
            "## Training Entry Mapping",
            "",
            "- Exp9 first gate should use `manifests/selected_primary_comp.repaired.jsonl`.",
            "- `train_mask_mode=partial`, `mask_from_manifest=true`, `loss_region_mode=full` first.",
            "- Stage1 DPO only; keep Stage2 frozen SFT/target-domain temporal weights.",
            "",
        ]
    )
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text("\n".join(lines), encoding="utf-8")


def main() -> int:
    args = build_parser().parse_args()
    output_root = Path(args.output_root).expanduser().resolve()
    manifests_dir = output_root / "manifests"
    report_path = Path(args.report).expanduser() if args.report else output_root / "reports" / "d3_post_generation_audit.md"
    if not manifests_dir.exists():
        raise SystemExit(f"[error] manifests dir not found: {manifests_dir}")

    manifest_rows = {}
    errors = {}
    manifest_counts = {}
    for name in d2.MANIFEST_NAMES:
        rows, row_errors = d2.read_jsonl(manifests_dir / name)
        manifest_rows[name] = rows
        errors[name] = row_errors
        manifest_counts[name] = len(rows) if not row_errors or rows else "MISSING"

    candidates = manifest_rows.get("candidates_all.jsonl", [])
    counters = {
        "status": dict(Counter(r.get("status") for r in candidates)),
        "source_dataset_before": dict(Counter(r.get("source_dataset") for r in candidates)),
        "generation_model": dict(Counter(r.get("generation_model") for r in candidates)),
        "generation_source_before": dict(Counter(r.get("generation_source") for r in candidates)),
        "diffueraser_stack_before": dict(Counter(r.get("diffueraser_inference_stack") for r in candidates)),
        "diffueraser_prior_before": dict(Counter(r.get("diffueraser_prior_mode") for r in candidates)),
    }
    stack, prior, evidence = d2.confirm_stack_prior(
        output_root,
        args.diffueraser_inference_stack,
        args.diffueraser_prior_mode,
    )

    repaired_files = []
    if not args.no_repair:
        for name in d2.REPAIR_MANIFEST_NAMES:
            repaired_rows = [repair_row(row, name, stack, prior, args) for row in manifest_rows.get(name, [])]
            repaired_name = name.replace(".jsonl", ".repaired.jsonl")
            repaired_path = manifests_dir / repaired_name
            d2.write_jsonl(repaired_path, repaired_rows)
            repaired_files.append(repaired_path)
            manifest_rows[repaired_name] = repaired_rows

    selected_comp = manifest_rows.get("selected_primary_comp.repaired.jsonl") or manifest_rows.get("selected_primary_comp.jsonl", [])
    selected_nocomp = manifest_rows.get("selected_primary_nocomp.repaired.jsonl") or manifest_rows.get("selected_primary_nocomp.jsonl", [])
    path_checks = {
        "selected_primary_comp": d2.path_audit(selected_comp, d2.PATH_FIELDS),
        "selected_primary_nocomp": d2.path_audit(
            selected_nocomp,
            ["final_loser_video_path", "raw_loser_video_path", "mask_path", "win_video_path"],
        ),
    }
    consistency = {
        "selected_primary_comp": d2.selected_consistency(selected_comp, "selected_primary_comp.jsonl"),
        "selected_primary_nocomp": d2.selected_consistency(selected_nocomp, "selected_primary_nocomp.jsonl"),
        "sample_id_unique_selected_primary_comp": len({r.get("sample_id") for r in selected_comp}) == len(selected_comp),
        "pair_index_count_selected_primary_comp": len({r.get("pair_index") for r in selected_comp}),
    }
    sample = d2.sample_audit(selected_comp, args) if selected_comp else {"sampled": 0, "issues": ["no selected_primary_comp rows"]}

    write_report(
        report_path,
        output_root,
        manifest_counts,
        errors,
        counters,
        path_checks,
        consistency,
        sample,
        stack,
        prior,
        evidence,
        repaired_files,
    )

    print(f"[d3-audit] output_root={output_root}")
    print(f"[d3-audit] report={report_path}")
    print(f"[d3-audit] stack={stack} prior={prior}")
    for name, count in manifest_counts.items():
        print(f"[d3-audit] {name} {count}")
    for repaired in repaired_files:
        print(f"[d3-audit] repaired={repaired}")
    if sample.get("issues"):
        print(f"[d3-audit] sample_issues={len(sample['issues'])}")
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
